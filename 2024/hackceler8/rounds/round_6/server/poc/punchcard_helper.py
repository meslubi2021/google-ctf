def character_to_array(i):
    punched = []
    # print(i)
    if i >= 0xf0:
        i -= 0xf0
        punched.append(i)
    if i >= 0xe0:
        i -= 0xe0
        punched.append(0)
    elif i >= 0xd0:
        i -= 0xd0
        punched.append(11)
    elif i >= 0xc0:
        i -= 0xc0
        punched.append(12)
    elif i >= 0x60:
        i -= 0x60
        punched.append(0)
    elif i >= 0x50:
        i -= 0x50
        punched.append(11)
    elif i >= 0x40:
        i -= 0x40
        punched.append(12)

    if i < 10:
        punched.append(i)
    elif i == 10:
        punched.extend([2,8])
    elif i == 11:
        punched.extend([3,8])
    elif i == 12:
        punched.extend([4,8])
    elif i == 13:
        punched.extend([5,8])
    elif i == 14:
        punched.extend([6,8])
    elif i == 15:
        punched.extend([7,8])
    return punched

def punched_to_row(punched):
    d = {12: 0, 11: 1, 0: 2, 1: 3, 2: 4, 3: 5, 4: 6, 5: 7, 6: 8, 7: 9, 8: 10, 9: 11}
    arr = [0] * 12
    for i in punched:
        idx = d[i]
        arr[idx] = 1
    return arr


def string_to_cpencoding(s):
    return s.encode('cp1141')

def transpose(matrix):
    return [list(row) for row in zip(*matrix)]

